    private void BotonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRegistrarActionPerformed
        if((textoIdentificacion.getText().length()==0)||
           (textoNombres.getText().length()==0)||
           (textoApellidos.getText().length()==0)||
           (textoTelefono.getText().length()==0)){
           JOptionPane.showMessageDialog(null,"Llenar todos los campos");
           }else{

            String estadocivil = null;
            if (radioSoltero.isSelected()){
                estadocivil = "Soltero";           
            }else if(radioCasado.isSelected()){
                estadocivil = "Casado";
            }else {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un estado civil");
            }
            
            String genero = null;
            if (radioMasculino.isSelected()){
                genero = "Masculino";           
            }else if(radioFemenino.isSelected()){
                genero = "Femenino";
            }else {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un genero");
            }

            Cargos c = (Cargos)ComboBoxCargos.getSelectedItem();
  
            empleado.setIdentificacion(textoIdentificacion.getText());
            empleado.setNombres(textoNombres.getText());
            empleado.setApellidos(textoApellidos.getText());
            empleado.setEstadoCivil(estadocivil);
            empleado.setGenero(genero);
            empleado.setTelefono(textoTelefono.getText());
            empleado.setCargo(c);
            empleado.setEstado("On");
            try {
                    ejc.create(empleado);
                    mensaje = "Empleado registrado";
                    JOptionPane.showMessageDialog(null, "Empleado registrado");
                } catch (Exception e) {
                    System.out.println("Mensaje en guardar: " + e.getMessage());
                    mensaje = "No se pudo registrar el empleado. \n" + e.getMessage();  
                    JOptionPane.showMessageDialog(null, "No se pudo registrar el empleado. \n" + e.getMessage());
                }
        }
           limpiarCampos();
    }//GEN-LAST:event_BotonRegistrarActionPerformed

