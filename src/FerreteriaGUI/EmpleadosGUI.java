    private void BotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonBuscarActionPerformed
        String identificacion = textoIdentificacion.getText();
        if((textoIdentificacion.getText().length()==0)){
           JOptionPane.showMessageDialog(null,"Ingrese el n° de identificación");
        }else{
        identificacion = textoIdentificacion.getText();
        Empleados c = ejc.findEmpleados(identificacion);
        if (c == null){

            limpiarCampos();

        }else
        {    
          if(c.getEstado().equals("Off")){
            JOptionPane.showMessageDialog(null,"Este empleado ha sido inhabilitado");
            
            limpiarCampos();
          }
        else  
        { 
          textoNombres.setText(c.getNombres());
          textoApellidos.setText(c.getApellidos());
          
            String estado = c.getEstadoCivil();
            if(estado.equals("Casado")){
               radioCasado.setSelected(true); 
            }else{
                radioSoltero.setSelected(true);
            }
            
            String genero = c.getGenero();
            if(genero.equals("Masculino")){
               radioMasculino.setSelected(true);
            }else{
                radioFemenino.setSelected(true);
            }
            
          textoTelefono.setText(c.getTelefono());  

            ComboBoxCargos.setSelectedItem(c.getCargo());
        }
        }}
    }//GEN-LAST:event_BotonBuscarActionPerformed

    private void textoIdentificacionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textoIdentificacionKeyTyped
        char c = evt.getKeyChar();
        if ((c < '0' || c > '9')){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Solo ingresar números");
        }
        int caracteres=10;
        if(textoIdentificacion.getText().length()>=caracteres){
            JOptionPane.showMessageDialog(null, "Sobrepasa la cantidad de números permitidos");
        }                 
    }//GEN-LAST:event_textoIdentificacionKeyTyped

